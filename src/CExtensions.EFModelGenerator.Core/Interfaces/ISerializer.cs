using CExtensions.EFModelGenerator.Core;

namespace CExtensions.EFModelGenerator.Common
{
    public interface ISerializer
    {
        /// <summary>
        /// Serializes the schema into a string. 
        /// The string could represent a list fo the tables or a complete source code file representation that has to 
        /// be written to disc by the TextWriter.
        /// </summary>
        /// <param name="schema"></param>
        /// <returns></returns>
        string Serialize(Schema schema);
    }
}